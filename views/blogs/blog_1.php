        <h2></h2>
        <!-- Intro -->
        <p style="padding: 40px 0 0 0;">
            Want to know what it's like to attend a code academy / boot camp? I wrote this article to answer the questions I wish were answered for me before I even applied.
        </p>
        <p>
            I want to keep the article as objective and fact-filled as possible. In another post I will give my personal opinion on specifics such as the worth of the program and whether or not it can compete with a <strong>college education</strong> or being <strong>self-taught</strong>.
            I attended <strong>Trilogy's Full-Time, Full-Stack Coding Boot Camp</strong> between February of 2019 and May of 2019. What I mean by <i>full-time</i> is that in order to get the most out of the program, it was expected that each student spend at least 40 hours outside of class working on homework, group projects, or studying. Looking back, I think this number was completely arbitrary and depends on how you learn and how much you already know.
            And the term <i>full-stack</i> implies that students learn how to build websites that use front-end and back-end technologies. I will explain in detail which languages, frameworks, and libraries we learned.
        </p>
        <!-- Average Day -->
        <h2>What Did The Average Day Look Like At The Code Academy?</h2>
        <p>The list belows outlines what an average weekday was like for me during the code academy.</p>
        <ul>
            <li>
                <strong>7:30 AM</strong> - Wake up and do morning routine, e.g., eat, get dressed, etc...
            </li>
            <li>
                <strong>7:55 AM</strong> - Begin commute to campus.
            </li>
            <li>
                <strong>8:45 AM</strong> - Show up to class. 
                Classes were held at the University of Texas' Thompson Conference Center. 
                I would show up early to catch up on studies or homework and to break up study blocks. I took to heart the 40 hours of work expectation because of the financial and time investment I was making. 
                That being said, I would have been burnt out early on if I did not divide my study time into digestible blocks.
            </li>
            <li>
                <strong>9:30 AM</strong> - Office hours begin. This is an opportunity for students to get one-on-one help from any of the 4 teaching assistants or the instructor.
                Unfortunately the TAs were usually about 10-15 minutes late, so it was difficult to get much face time.
            </li>
            <li>
                <strong>10:00 AM</strong> - Class would begin. We would usually start class by doing a whiteboard question. 
                We could choose to work alone or with partners. 
                After about 15 minutes of discussion, the instructor would ask someone to share their answer and move on with class.
                <br>
                What we did after the warm-up depended on whether it was an <i>instruction week</i> or a <i>project week</i>. 
                If it was an instruction week, the instructor would lecture about technology. 
                I will get into the details later down the page.
                If it was a project week, we would put our tables together with our group members and work on our project. 
                TAs would float around the class and answer questions or sometimes sit in the back and play fortnite.
            </li>
            <li>
                <strong>12:00 PM</strong> - Lunch. I would usually spend about 15-20 minutes eating and chatting with classmates. 
                Most people would buy from the cafeteria. Some people would go off campus, and some would bring their lunch.
                The remaining time was spent catching up on studies and homework.
            </li>
            <li>
                <strong>12:45 PM</strong> - Class time again. Like the morning, this time was used for lecture or project work.
            </li>
            <li>
                <strong>2:30 PM</strong> - End of class and beginning of afternoon office hours. 
                Most people would leave at this time, but some of us would stick around to work on homework or get help from TAs.
                Sometimes a TA would offer to help us by hosting whiteboard questions.
            </li>
            <li>
                <strong>3:00 PM</strong> - Leave campus. 
            </li>
            <li>
                <strong>4:00 PM</strong> - Get home, eat an early dinner, and then it was back to doing homework and projects. 
                I would work from home, go to a coffee shop, or meet up with classnames.
            </li>
            <li>
                <strong>10:00 PM</strong> - Stop working on code and i don't know, try to actually live a life? 
                This time would change depending on the difficulty and length of the current assignment, but I tried to always have my laptop put away by 11:00 PM.
            </li>
            <li>
                <strong>12:00 AM</strong> - Go to sleep.
            </li>
        </ul>
        <p>As you can see, the boot camp consumed all of my time. 
        As i mentioned before, this was a huge financial investment for me, and I was determined to make every minute count while I had access to these resources. 
        Not everyone's experience was the same, and what a student gets out of the program depends on what they put in.
        </p>
        <!-- Teaching Staff -->
        <h2>Who Were The Teaching Staff?</h2>
        <p>Each cohort has one instructor. The requirement to become a Trilogy Instructor is he or she must be an "industry professional". 
        Instructors "deliver ... course curriculum, facilitate learning, and are responsible for ensuring an outstanding learning experience for the students in their class section."
        You can find more about the position <a href="https://www.trilogyed.com/instructors/" target="_blank" class="contentLinks">on Trilogy's instructor page.</a>
        </p>
        <p>Our cohort had five TAs. All of them were graduates of the Trilogy boot camp. 
        From what I understand, being a TA is a decent way for graduates to gain experience before getting a job. All of our TAs were looking for jobs, and one of them actually got one halfway through the semester. </p>
        <!-- Tech Used -->
        <h2>Which Technologies Did You Learn?</h2>
        <p>We would typically spend 2-3 days learning each of these technologies. I will list them below in the order in which we learned them with a small description of each.</p>
        <ul>
            <li><strong>HTML (HyperText Markup Language) </strong>- HTML is probably the most essential technology you can learn in order to be a web developer.
            HTML is the content you see on a page, e.g., the paragraphs, headers, and images.</li>
            <li><strong>CSS (Cascading Style Sheets) </strong>- CSS makes up the styling of a website. This includes the color of the background and text, how and where images are places, some animations, and more.</li>
            <li><strong>JS (JavaScript) </strong>- JavaScript is a scripting language for web development. JavaScript is used for updating the DOM, animations, API calls, routing, and much more.</li>

        </ul>
        <img src="../../assets/images/house_analogy.jpg" style="width: 80%; height: auto; cover: contain; margin: 50px auto 50px auto; display: block;" alt="HTML CSS JS">
        <p><i>I like to think of these three technologies as parts of a house. HTML is what makes up the house: the foundation and frame. 
        CSS is how you design the house: the color of the walls, the style of the materials, and anything else aesthetic.
        JS is what makes the house functional: the pipes for water, wiring for electricity, locks on the doors, and any other gadget.</i></p>
        <ul>
            <li><strong>Git </strong>- Git is a version-control system for tracking changes in source code. 
            It is especially useful when working in groups. 
            From my experience of collaborating <strong>without</strong> version control, which was an absolute nightmare,  
            it allows you to track changes and handle problems, like merge conflicts.</li>
            <li><strong>jQuery </strong>- jQuery is one of the most popular JavaScript libraries. It simplifies <i>how</i> the DOM is updated, API calls, and much more. </li>
            <li><strong>AJAX (or API calls in general) </strong>- AJAX is specifically a jQuery method for making asynchronous HTTP requests. 
            API calls in general are so important. 
            Want to pull data from an API or database? 
            Want to add information to a database? 
            Change information in a database? 
            You will use APIs so often in the industry and when designing your own applications.</li>
            <li><strong>Firebase </strong>- Firebase is Google's mobile platform for development. We only learned about how to use Firebase's remote database. 
            We learned about the CRUD actions within the scope of Firebase.
            CRUD refers to the four basic actions you can do with a database: Create, Read, Update, and Delete.</li>
            <li><strong>Node.js </strong>- Node is a runtime environment that executes JavaScript code outside of the browser. 
            This is important when setting up the back-end of an application.
            Some of the things you can do with Node are set up routes, run JS code, and so much more.</li>
            <li><strong>MySQL </strong>- MySQL is an open-source relational database management system. Simply put it's a database. 
            From my experience MySQL is the most popular database. 
            Databases are especially important for persisting data. 
            Want to be able to make changes to your website and have those changes persist? Want to store credentials? You will need a database.</li>
            <li><strong>Express.js </strong>- Express is a Node web framework. 
            It makes routing easy. 
            It helps build the bridge that carries data between the client and the database. 
            I use this for all of my full-stack applications.</li>
            <li><strong>Handlebars.js </strong>- Probably the least used technology on this list, Handlebars takes HTML and compiles it into a JavaScript function. </li>
            <li><strong>Sequelize </strong>- Sequelize is a promise-based ORM (object-relational mapping) for Node.js. I have found this to be a very useful tool because it helps manage database queries for multiple tables.</li>
            <li><strong>MongoDB </strong>- Like MySQL, this is a database. Unlike MySQL, MongoDB is scalable and flexible.</li>
            <li><strong>Mongoose </strong>- Mongoose is an ODM (object data modeling) library. It provides a model for data and is still flexible.</li>
            <li><strong>React.js</strong>- React is Facebook's JavaScript Library. 
            It will take everything you know about the HTML-CSS-JS architecture and flip it on its head. 
            It is especially useful for changing data that needs to be recorded on the front end, reuse components, and much more.</li>
            <li><strong>PHP (Personal Home Page or recursive for Hypertext Preprocessor) </strong>- PHP is a general-purpose scripting language. 
            Like JavaScript, it can be used for both front-end and back-end applications. It's fast, open source, and easy to use.</li>
            <li><strong>Laravel </strong>- Laravel is a PHP Framework. It speeds up the time it takes to solve common tasks.</li>
            <li><strong>Computer Science Fundamentals </strong>- We spent some time learning big O, data types, different types of data searches and sorts.</li>
        </ul>
        <img src="../../assets/images/MERN-Stack.jpg" style="width: 80%; height: auto; cover: contain; margin: 50px auto 50px auto; display: block;" alt="MERN Stack">
        <p>A couple that were not listed above are <a href="https://github.comstructors/" target="_blank" class="contentLinks">GitHub</a> and <a href="https://www.heroku.com/" target="_blank" class="contentLinks">Heroku.</a> In short GitHub is a software development platform with a large community. Heroku is primarily used for deploying full-stack applications.</p>
        <p>We learned a lot of technologies during the code academy. 
        That being said, were any of us experts on all of these? 
        Definitely not. I will share more of my opinions on this in the next blog post, but it should be obvious that the coding boot camp was a decent place to start in order to learn the basics of a lot of different technologies.</p>
